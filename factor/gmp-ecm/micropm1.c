        {
            if (b1_100_steps[i] == 0)
            {
                pgiant = upm1_mulredc(pgiant, pw, n, rho);
                i++;
            }
            acc = upm1_mulredc(acc, upm1_submod(pgiant, d[upm1_map[b1_100_steps[i]]], n), n, rho);
        }
